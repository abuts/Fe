function [a0,slope,a_interp,delta]=FeT_expansion(Ti)
% calculate Temperature dependance of the lattice parameters ol Alplha Fe 
% from the results, published at:
%   Basinski, Z. S., Hume-Rothery, W. & Sutton, A. L. 
%   The Lattice Expansion of Iron. 
%  Proc. R. Soc. Lond. Ser. Math. Phys. Sci. 229, 459 (1955).
source =...
    [1189	12.1750;...
    1175	12.1684;...
    1120	12.1347;...
    1060	12.1080;...
    1043    12.0964;...
    1033    12.0920;...
    1026    12.0911;...
    994     12.0722 ;...
    921	12.0304;...
    822	11.9742;...
    722	11.9199;...
    623	11.8678;...
    513	11.8104;...
    293	11.7024;...
    225	11.6760;...
    131	11.6460;...
    92	11.6360;...
    73	11.6330;...
    20	11.6310];
T = source(:,1); % Tn in K
a = (source(:,2)*2).^(1/3); % lattice parameter (A)

[p,S]=polyfit(T,a,3) ;
a0 = p(end);
slope = p(end-1);
if exist('Ti','var')
    [a_interp,delta] = polyval(p,Ti,S);
else
    delta = [];
    a_interp  = [];
end
